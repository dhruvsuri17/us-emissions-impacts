"""
Script to process CEMS data for different ISOs and create metadata files.
"""

import os
import pandas as pd

def highest_gross_load_per_unit(df):
    """
    Find the unit with the highest gross load for each facility.
    
    Parameters:
        df (DataFrame): DataFrame with facility and unit data
        
    Returns:
        DataFrame: DataFrame with the highest gross load unit per facility
    """
    # Group by Facility ID and Unit ID, get the maximum gross load for each unit
    max_gross_load_per_unit = df.groupby(['Facility ID', 'Unit ID'])['Gross Load (MW)'].max().reset_index()
    
    # For each facility, find the unit with the highest gross load
    highest_gross_load_per_facility = max_gross_load_per_unit.loc[max_gross_load_per_unit.groupby('Facility ID')['Gross Load (MW)'].idxmax()]
    
    return highest_gross_load_per_facility

def process_iso_data(directory, facility_ids, output_file):
    """
    Function to process CEMS data for a specific ISO.
    
    Parameters:
        directory (str): Path to the directory containing the ISO CSV files.
        facility_ids (list): List of Facility IDs to filter the data.
        output_file (str): Output CSV file path where processed data will be saved.
    """
    
    # List all CSV files in the directory
    csv_files = [f for f in os.listdir(directory) if f.endswith('.csv')]

    # Initialize an empty list to hold all data
    df_list = []

    # Columns of interest
    cols = ['Facility ID', 'Unit ID', 'Date', 'Hour', 'Operating Time', 'Gross Load (MW)', 
        'SO2 Mass (lbs)', 'CO2 Mass (short tons)', 'NOx Mass (lbs)', 'Heat Input (mmBtu)',
        'Primary Fuel Type', 'Unit Type']

    # Read and process each CSV file
    for file in csv_files:
        file_path = os.path.join(directory, file)
        df = pd.read_csv(file_path, low_memory=False)
        
        # Select only the relevant columns
        df = df[cols]
        df_list.append(df)

    # Concatenate all dataframes into one
    df = pd.concat(df_list, ignore_index=True)

    # Convert 'Date' and 'Hour' to datetime
    df['Date'] = pd.to_datetime(df['Date'])
    df['Hour'] = pd.to_timedelta(df['Hour'], unit='h')
    df['Date'] = df['Date'] + df['Hour']

    # Drop the unnecessary columns
    df = df.drop(columns=['Hour'])

    # Filter by Facility IDs
    df = df[df['Facility ID'].isin(facility_ids)]

    # Group by Date, Facility ID, and Unit ID, then sum the values
    df = df.groupby(['Date', 'Facility ID', 'Unit ID']).sum().reset_index()

    # Filter the date range between 2019-01-01 and 2023-12-31
    df = df[(df['Date'] >= '2019-01-01') & (df['Date'] <= '2023-12-31')]

    # Get the highest gross load unit per facility
    highest_gross_load_per_facility = highest_gross_load_per_unit(df)
    
    # Get facility and unit types information
    df_with_types = df[['Facility ID', 'Unit ID', 'Primary Fuel Type', 'Unit Type']].drop_duplicates()
    
    # Merge the highest gross load data with facility/unit types
    result = highest_gross_load_per_facility.merge(df_with_types, on=['Facility ID', 'Unit ID'], how='left')

    # Save the result
    result.to_csv(output_file, index=False)
    print(f"Processed data saved to {output_file}")

